/* Condicional if e else

1. Faça uma condicional if para verificar se a é maior que b

2. Se for: mude o valor de resultado para "É maior"

3. Se não for: mude o valor de resultado para "É menor"

4. Note que a segunda mudança precisa estar em um else

5. Imprima o valor de resultado após a condicional */

const a = 10;
const b = 15;
let resultado;

if (a > b) {
  resultado = "È maior";
} else {
  resultado = "È menor";
}

console.log(resultado);
